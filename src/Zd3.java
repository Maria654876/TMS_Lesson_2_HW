public class Zd3 {
    /*
    В данном методе объявлены две переменные и присвоены произвольные числовые значения.
    Для первой переменной вызван инкремент.
    Для второй переменной вызван декримент.
    Результат выводится на консоль.
     */
    public static void main(String[] args) {
        int a=6;
        int b=9;
        a++;
        b--;
        System.out.println(a);
        System.out.println(b);
    }
}
